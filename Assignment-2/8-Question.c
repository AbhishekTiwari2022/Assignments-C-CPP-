//8. Write a program to print size of an int, a float, a char and a double type variable
#include<stdio.h>
int main()
{
    int x;
    float y;
    char s;
    double z;
    printf("Size Of Int Is %d\n",sizeof(x));

    printf("Size Of Float Is %d\n",sizeof(y));

    printf("Size Of Char Is %d\n",sizeof(s));

    printf("Size Of Double Is %d\n",sizeof(z));
    return 0;
}
